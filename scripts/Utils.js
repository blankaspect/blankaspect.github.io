// CLASS: UTILITY METHODS


////////////////////////////////////////////////////////////////////////
//  Constructor
////////////////////////////////////////////////////////////////////////

function Utils()
{
	throw new Error("Cannot instantiate");
}

//----------------------------------------------------------------------

////////////////////////////////////////////////////////////////////////
//  Class methods
////////////////////////////////////////////////////////////////////////

Utils.contains = function(object, value)
{
	var found = false;
	for (var property in object)
	{
		if (object[property] == value)
		{
			found = true;
			break;
		}
	}
	return found;
};

//----------------------------------------------------------------------

Utils.nextRandomInt = function(range)
{
	return Math.floor(Math.random() * range);
};

